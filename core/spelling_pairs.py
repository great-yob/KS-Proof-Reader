def find_spelling_fixes(text: str) -> list:
    """비표준 활용 어간이 든 어절을 찾아 [(original, corrected, 사유), …]로 반환한다.

    어절 단위로 훑어 _STEM_PAIRS의 비표준 어간이 부분문자열로 있으면 그 부분만 규범 어간으로
    치환한다('아니였으며,'→'아니었으며,'). 중복 어절 제거. 비표준 어간이 정상 단어엔 없으므로
    위치 무관 substring 치환이 안전하다. 매칭 없으면 빈 리스트.
    """
    out, seen = [], set()
    for w in text.split():
        if w in seen:
            continue
        seen.add(w)
        for bad, good in _STEM_PAIRS.items():
            if bad in w:
                fixed = w.replace(bad, good)
                if fixed != w:
                    out.append((w, fixed,
                                f"'{bad}'은 비표준 표기 — 규범 표기 '{good}'"))
                break
    return out


