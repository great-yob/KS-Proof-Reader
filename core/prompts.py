def build_integrated_prompt(text: str, suspicious_words: list[str],
                            glossary: list[tuple] | None = None) -> str:
    """사전 검증 결과 + 청크 간 글로서리와 결합한 통합 교정 프롬프트.

    ⚠ 어문 규범(KAGEC) 컨텍스트 주입은 제거됨(2026-06-22) — 실측에서 규범 블록이 AI의
    오탈자 탐지 주의를 분산시켜 멀쩡히 잡던 오타(고지사→고지서·훗가이도→홋카이도)를 놓치는
    회귀가 확인됨(eomun.db 비활성 시 복귀로 입증). KoGEC 제거와 동일 원칙. 어문 규범은
    결정론 페어(eomun_pairs)·골드셋으로만 사용한다. 재도입은 정량 검증(precision/F0.5) 선결.

    Args:
        text:             검사 대상 청크
        suspicious_words: 사전 스크리닝 의심 단어
        glossary:         이전 청크에서 확정된 (original, corrected) 튜플 리스트
    """
    sw = "\n".join(f"- {w}" for w in suspicious_words) if suspicious_words else "- (의심 단어 없음)"
    if glossary:
        # 정렬해 주입 — 누적 순서(상류 청크의 탐지 순서)에 프롬프트가 흔들리지 않게 한다.
        #   항목은 gemini_checker._normalize_glossary_entry로 base 정규화·중복 제거된 상태.
        gl = "\n".join(f'- "{orig}" → "{corr}"' for orig, corr in sorted(glossary))
    else:
        gl = "- (아직 확정된 교정 없음 — 첫 청크이거나 새 단어들이 처음 등장)"
    return PROMPT_INTEGRATED.format(text=text, suspicious_words=sw, glossary=gl)
